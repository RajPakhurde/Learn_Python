# OPERATORS IN PYTHON
print(10 + 3)  # out:- 13
print(10 - 3)  # out:- 7
print(10 / 3)  # out:- 3.333333333
print(10 // 3)  # out:- 3 "if we want int value after division we use '//' operator"
print(10 ** 3)  # out:- 1000 "10^3"
print(10 % 3)  # out:- 1

