import math
# Python has different modules in order to use that we have to import that first and using object name we have to access all the methods present in that module.
# In this case math is object name
print(math.ceil(2.9))  # out:- 3
print(round(2.9))  # out:- 3
print(math.floor(2.9))  # out:- 2
print(abs(-2.9))  # out:- 2.9
